public class SQRT {
    public static void main(String[] args) {
        int a = 6;
        double res = Math.pow(a, 0.5);//1 variant(sqrt of X number(like 6) is X to the power of 1/2 or 0.5)
        double res2 = Math.sqrt(a);//2 variant(better and shorter)
        System.out.println(res);
        System.out.println(res2);
    }
}
